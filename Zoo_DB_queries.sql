CREATE TABLE  СПІВРОБІТНИКИ 
(ТАБ_НОМЕР VARCHAR (3) NOT NULL  PRIMARY KEY, 
ПІП  VARCHAR (50) NOT NULL ,
СТАТЬ  VARCHAR (8) NOT NULL ,
ВІК  VARCHAR (3) NOT NULL ,
СТАЖ  VARCHAR (3) NOT NULL,
АДРЕСА VARCHAR (50) NOT NULL); 

CREATE TABLE ПОСАДА  
(ТАБ_НОМЕР  VARCHAR (3) NOT NULL  PRIMARY KEY,
КОД_ПОСАДИ  VARCHAR (3) NOT NULL ,
НАЙМЕН_ПОСАДИ VARCHAR (25) NOT NULL,
ЗАР_ПЛАТА VARCHAR (25) NOT NULL); 

CREATE TABLE ТВАРИНИ 
(НОМЕР_ТВАР VARCHAR (5) NOT NULL  PRIMARY KEY, 
--ВИД_ТВАР VARCHAR (15) NOT NULL FOREIGN KEY REFERENCES СУМІСНІСТЬ_ТВАРИН (ВИД_ТВАР),  
КЛИЧКА_ТВАР  VARCHAR (15) NOT NULL , 
СТАТЬ_ТВАР  VARCHAR (10) NOT NULL , 
ВІК_ТВАР VARCHAR (5) NOT NULL, 
РІСТ_СМ VARCHAR (5) NOT NULL , 
ВАГА_КГ VARCHAR (10) NOT NULL, 
ДАТА_ПОСТУП  DATE);

CREATE TABLE ВІДВІДУВАЧІ
(ПІП_ВІДВ VARCHAR (50) NOT NULL ,
НОМЕР_КВИТКА VARCHAR (5) NOT NULL PRIMARY KEY,
ДАТА_ВІДВІД DATE);      

CREATE TABLE ПРАЦЮЮТЬ_З_ТВАРИНАМИ 
(ТАБ_НОМЕР VARCHAR (3) NOT NULL  PRIMARY KEY REFERENCES ПОСАДА (ТАБ_НОМЕР),
НОМЕР_ТВАР  VARCHAR (5) NOT NULL REFERENCES ТВАРИНИ (НОМЕР_ТВАР), 
ДАТА_ПРИСТУП  DATE NOT NULL); 

CREATE TABLE ПЕРЕБУВАННЯ_ТВАРИН 
(НОМЕР_ТВАР VARCHAR (5) NOT NULL REFERENCES ТВАРИНИ (НОМЕР_ТВАР),
ВИД_ТВАР VARCHAR (15) NOT NULL , 
НОМЕР_ЖИТЛА VARCHAR (3)  NOT NULL PRIMARY KEY,
ВИД_ЖИТЛА VARCHAR (30) NOT NULL); 

CREATE TABLE КАРТКА_ХВОРОБ_ТВАРИНИ 
(НОМЕР_ТВАР VARCHAR (5) NOT NULL  PRIMARY KEY  REFERENCES ТВАРИНИ (НОМЕР_ТВАР), 
ХВОРОБА VARCHAR (20) ,
ДАТА_ХВОР  DATE); 

CREATE TABLE КАРТКА_ПРИВИВОК_ТВАРИНИ 
(НОМЕР_ТВАР VARCHAR (5) NOT NULL REFERENCES ТВАРИНИ (НОМЕР_ТВАР), 
ПРИВИВКА VARCHAR (5), 
ДАТА_ПРИВИВ  DATE); 

CREATE TABLE  ЗООПАРКИ 
(НАЗВА_ЗООПАРКА VARCHAR (50) NOT NULL, 
НОМЕР_ТВАР  VARCHAR (5) NOT NULL PRIMARY KEY REFERENCES ТВАРИНИ (НОМЕР_ТВАР), 
ПЕРЕДАЧА VARCHAR (30), 
ДАТА_ОБМІНУ DATE, 
ОТРИМАЛИ  VARCHAR (15));     

CREATE TABLE  ПОТОМСТВО 
(НОМЕР_ТВАР VARCHAR (5) NOT NULL   REFERENCES ТВАРИНИ (НОМЕР_ТВАР), 
НОМЕР_ДИТ  VARCHAR (5) NOT NULL PRIMARY KEY, 
ПАРА VARCHAR (5) NOT NULL, 
ДАТА_НАРОДЖ DATE); 

CREATE TABLE  СУМІСНІСТЬ_ТВАРИН   
(ВИД_ТВАР VARCHAR (15) NOT NULL PRIMARY KEY, 
СУМІСНІСТЬ  VARCHAR (20)); 

CREATE TABLE  ГОДУВАННЯ 
(ВИД_ТВАР  VARCHAR (15) NOT NULL  REFERENCES СУМІСНІСТЬ_ТВАРИН (ВИД_ТВАР), 
ТИП_КОРМА  VARCHAR (50) NOT NULL  PRIMARY KEY, 
СЕЗОН  VARCHAR (20)); 

CREATE TABLE  ПОСТАЧАЛЬНИКИ 
(НОМЕР_ПОСТАЧ VARCHAR (5) NOT NULL  PRIMARY KEY, 
НАЗВА_ОРГАНІЗ VARCHAR (25) NOT NULL); 

CREATE TABLE  КОРМА 
(НОМЕР_ПОСТАЧ  VARCHAR (5) NOT NULL REFERENCES ПОСТАЧАЛЬНИКИ (НОМЕР_ПОСТАЧ), 
ТИП_КОРМА VARCHAR (50) NOT NULL REFERENCES ГОДУВАННЯ (ТИП_КОРМА), 
КІЛЬК_КОРМА VARCHAR (10) NOT NULL, 
ЦІНА_КОРМА_КГ  VARCHAR (25));
